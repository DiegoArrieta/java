    private void btnMasterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMasterActionPerformed
        try
        {
            int num1, num2, s,r,m,d;
            MetodosCalculos x=new MetodosCalculos();
            num1=Integer.parseInt(txtNum1.getText());
            num2=Integer.parseInt(txtNum2.getText());
            s=x.suma(num1, num2);
            r=x.restar(num1, num2);
            m=x.multiplicar(num1, num2);
            d=x.dividir(num1, num2);
            //lblResultado.setText("Resultado: "+calculo);
            resultado(s);
            resultado(r);
            resultado(m);
            resultado(d);
        }
        catch(NumberFormatException ex)
        {
            JOptionPane.showConfirmDialog(null, "Favor ingresar numeros", "Error", 0);
            
        }
    }//GEN-LAST:event_btnMasterActionPerformed
    private void limpiar()
    {
        txtNum1.setText("");
        txtNum2.setText("");
        txtNum1.requestFocus();
        lblResultado.setText("Resultado:");
    }
    private void resultado(int numero)
    {
        JOptionPane.showConfirmDialog(null, "El resultado es: "+numero, "Error", 1);
    }
