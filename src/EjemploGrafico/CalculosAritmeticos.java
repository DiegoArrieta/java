    private void btnSumarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSumarActionPerformed
        try
        {
            int num1, num2, adicion;
            MetodosCalculos x=new MetodosCalculos();
            num1=Integer.parseInt(txtNum1.getText());
            num2=Integer.parseInt(txtNum2.getText());
            adicion=x.suma(num1, num2);
            lblResultado.setText("Resultado: "+adicion);
            
           
        }
        catch(NumberFormatException ex)
        {
            JOptionPane.showConfirmDialog(null, "Favor ingresar numeros", "Error", 0);
            limpiar();
        }
    }//GEN-LAST:event_btnSumarActionPerformed

    private void btnDividirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDividirActionPerformed
        try
        {
            int num1, num2, calculo;
            MetodosCalculos x=new MetodosCalculos();
            num1=Integer.parseInt(txtNum1.getText());
            num2=Integer.parseInt(txtNum2.getText());
            if(num1 == 0 || num2 == 0)
            {
                JOptionPane.showConfirmDialog(null, "No existe Division por CERO", "error2", 1);
                limpiar();
            }
            calculo=x.dividir(num1, num2);
            lblResultado.setText("Resultado: "+calculo);
            
        }
        catch(NumberFormatException ex)
        {
            JOptionPane.showConfirmDialog(null, "Favor ingresar numeros", "Error", 0);
            limpiar();
        }
    }//GEN-LAST:event_btnDividirActionPerformed

