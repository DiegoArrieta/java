public class MetodosCalculos {
    public int suma(int num1, int num2)
    {
        int calculo;
        calculo = (num1+num2);
        return calculo;
    }
    
    public int restar(int num1, int num2)
    {
        int calculo;
        calculo = (num1-num2);
        return calculo;
    }
    public int multiplicar(int num1, int num2)
    {
        int calculo;
        calculo = (num1*num2);
        return calculo;
    }
    public int dividir(int num1, int num2)
    {
        int calculo;
        calculo = (num1/num2);
        return calculo;
    }
    
}
